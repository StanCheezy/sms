    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)table.getModel();
        int row = table.getSelectedRow();
       
        phone_number.setText(model.getValueAt(row, 0).toString());
        fname.setText(model.getValueAt(row, 1).toString());
        lname.setText(model.getValueAt(row, 2).toString());
        
        String comboBox = model.getValueAt(row, 3).toString();
        for(int i=0; i < gender.getItemCount(); i++){
            if(gender.getItemAt(1).toString().equalsIgnoreCase(comboBox)){
                gender.setSelectedIndex(i);
            }
        }
        dob.setText(model.getValueAt(row, 3).toString());
        email.setText(model.getValueAt(row, 4).toString());
        country.setText(model.getValueAt(row, 5).toString());
        city.setText(model.getValueAt(row, 6).toString());
    }//GEN-LAST:event_tableMouseClicked

