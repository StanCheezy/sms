    public ArrayList<sms> userlist(){
        ArrayList<sms> userslist = new ArrayList<>();
        
        try {
            String url = "jdbc:mysql://localhost:3306/sms";
            String un = "root";
            String pas = "root";
            conn = DriverManager.getConnection(url, un, pas);
            
            String query1 = "SELECT * FROM register";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(query1);
            sms user;
            while(rs.next()){
                user = new sms(rs.getString("phone_number"),rs.getString("fname"),rs.getString("lname"),rs.getString("gender"),
                        rs.getString("dob"),rs.getString("email"),rs.getString("country"),rs.getString("city"));
                
                userslist.add(user);
                
            }
            
    }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        return userslist;
    }
    
